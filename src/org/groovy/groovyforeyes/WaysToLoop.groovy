//Using Upto - Executes from 0 to 2
0.upto(2) { print "$it "}

//Times - Executes the loop that many times
36.times { print "$it "}

//Step - Prints values starting from 0 and less than 10 (it means 10 will not be printed by stepping 2 values
0.step(12, 2) { print "$it "}

